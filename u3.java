public class EmployeeWage {

    public static void main(String[] args) {
        // Constants
        int wagePerHour = 20;
        int fullDayHour = 8;

        // Calculate daily wage
        int dailyWage = wagePerHour * fullDayHour;

        // Display the daily wage
        System.out.println("Employee Daily Wage is: $" + dailyWage);
    }
}
